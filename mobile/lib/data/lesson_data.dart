// Lesson data ported from the frontend LearningScreen.jsx
// Contains all lessons with their slides: quiz, teach, and pronounce types.

class LessonSlide {
  final String type; // 'quiz', 'teach', 'pronounce'
  final String? subtype; // 'intro', 'char_select'
  final String? badge;
  final String? title;
  final String? question;
  final String? mainChar;
  final String? audioText;
  final String? hint;
  final String? instruction;
  final List<String>? options;
  final String? answer;
  final bool isReview;

  const LessonSlide({
    required this.type,
    this.subtype,
    this.badge,
    this.title,
    this.question,
    this.mainChar,
    this.audioText,
    this.hint,
    this.instruction,
    this.options,
    this.answer,
    this.isReview = false,
  });

  LessonSlide copyWithReview() => LessonSlide(
    type: type,
    subtype: subtype,
    badge: badge,
    title: title,
    question: question,
    mainChar: mainChar,
    audioText: audioText,
    hint: hint,
    instruction: instruction,
    options: options,
    answer: answer,
    isReview: true,
  );
}

class Lesson {
  final String title;
  final List<LessonSlide> slides;

  const Lesson({required this.title, required this.slides});
}

// --- Helpers ---
List<LessonSlide> _charPair(
  String hindiChar,
  String englishSound,
  String hintText,
  List<String> optionsEnglish,
  List<String> optionsHindi,
) {
  return [
    LessonSlide(
      type: 'quiz',
      subtype: 'intro',
      badge: 'New Character',
      title: 'New Letter',
      question: 'What sound does this letter make?',
      mainChar: hindiChar,
      audioText: hindiChar,
      hint: hintText,
      options: optionsEnglish,
      answer: englishSound,
    ),
    LessonSlide(
      type: 'quiz',
      subtype: 'char_select',
      question: "Select the correct character for '$englishSound'",
      audioText: hindiChar,
      options: optionsHindi,
      answer: hindiChar,
    ),
  ];
}

List<LessonSlide> _vocabPair(
  String hindiWord,
  String englishMeaning,
  String hintText,
  List<String> optionsEnglish,
  List<String> optionsHindi,
) {
  return [
    LessonSlide(
      type: 'quiz',
      subtype: 'intro',
      badge: 'New Word',
      title: 'New Vocabulary',
      question: 'What does this word mean?',
      mainChar: hindiWord,
      audioText: hindiWord,
      hint: hintText,
      options: optionsEnglish,
      answer: englishMeaning,
    ),
    LessonSlide(
      type: 'quiz',
      subtype: 'char_select',
      question: "Select the Hindi word for '$englishMeaning'",
      options: optionsHindi,
      answer: hindiWord,
    ),
  ];
}

